package cmd.vo;


public class SitVO
{
	private String sit_no;
	private String member_no;
	private String sit_id;
	private String use_yn;
	private String insertDate;
	
	public String getInsertDate() {
		return insertDate;
	}
	public void setInsertDate(String insertDate) {
		this.insertDate = insertDate;
	}
	public String getSit_no() {
		return sit_no;
	}
	public void setSit_no(String sit_no) {
		this.sit_no = sit_no;
	}
	public String getMember_no() {
		return member_no;
	}
	public void setMember_no(String member_no) {
		this.member_no = member_no;
	}
	public String getSit_id() {
		return sit_id;
	}
	public void setSit_id(String sit_id) {
		this.sit_id = sit_id;
	}
	public String getUse_yn() {
		return use_yn;
	}
	public void setUse_yn(String use_yn) {
		this.use_yn = use_yn;
	}
	@Override
	public String toString() {
		return "SitVO [sit_no=" + sit_no + ", member_no=" + member_no + ", sit_id=" + sit_id + ", use_yn=" + use_yn
				+ ", insertDate=" + insertDate + "]";
	}
}
